#!/usr/bin/env node

import { mkdirSync, readdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join, relative, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const protoRoot = join(repoRoot, "proto");
const outputRoot = join(repoRoot, "ts/packages/core/src/gen");

const scalarSchemas = new Map([
  ["string", "z.string()"],
  ["bool", "z.boolean()"],
  ["double", "z.number()"],
  ["float", "z.number()"],
  ["int32", "z.number().int()"],
  ["sint32", "z.number().int()"],
  ["sfixed32", "z.number().int()"],
  ["uint32", "z.number().int()"],
  ["fixed32", "z.number().int()"],
  ["int64", "z.string()"],
  ["sint64", "z.string()"],
  ["sfixed64", "z.string()"],
  ["uint64", "z.string()"],
  ["fixed64", "z.string()"],
  ["bytes", "z.instanceof(Uint8Array)"],
  ["google.protobuf.Value", "z.any()"],
  ["google.protobuf.Struct", "z.record(z.string(), z.any())"],
  ["google.protobuf.ListValue", "z.array(z.any())"],
  ["google.protobuf.Empty", "z.object({})"],
  ["google.protobuf.Timestamp", "z.string()"],
  ["google.protobuf.Duration", "z.string()"],
  ["google.protobuf.Any", "z.record(z.string(), z.any())"],
]);

const generatedHeader = `// Code generated by scripts/generate-proto-zod.mjs. DO NOT EDIT.
`;

function walkProtoFiles(dir) {
  return readdirSync(dir, { withFileTypes: true }).flatMap((entry) => {
    const absolutePath = join(dir, entry.name);

    if (entry.isDirectory()) {
      return walkProtoFiles(absolutePath);
    }

    return entry.isFile() && entry.name.endsWith(".proto") ? [absolutePath] : [];
  });
}

function stripLineComment(line) {
  const commentIndex = line.indexOf("//");
  return commentIndex === -1 ? line : line.slice(0, commentIndex);
}

function parseProtoFile(filePath) {
  const source = readFileSync(filePath, "utf8");
  const lines = source.split(/\r?\n/);
  const packageName = source.match(/^package\s+([A-Za-z0-9_.]+);/m)?.[1] ?? "";
  const definitions = [];

  for (let index = 0; index < lines.length; index += 1) {
    const line = stripLineComment(lines[index]).trim();
    const enumMatch = line.match(/^enum\s+([A-Za-z0-9_]+)\s*\{/);
    const messageMatch = line.match(/^message\s+([A-Za-z0-9_]+)\s*\{/);

    if (enumMatch?.[1]) {
      const { definition, nextIndex } = parseEnum(lines, index, enumMatch[1]);
      definitions.push(definition);
      index = nextIndex;
      continue;
    }

    if (messageMatch?.[1]) {
      const { definition, nextIndex } = parseMessage(lines, index, messageMatch[1]);
      definitions.push(definition);
      index = nextIndex;
    }
  }

  return {
    filePath,
    packageName,
    definitions,
  };
}

function parseEnum(lines, startIndex, name) {
  const values = [];
  let depth = 0;

  for (let index = startIndex; index < lines.length; index += 1) {
    const line = stripLineComment(lines[index]).trim();
    depth += count(line, "{");
    depth -= count(line, "}");

    const valueMatch = line.match(/^([A-Z][A-Z0-9_]*)\s*=\s*\d+\s*;/);
    if (valueMatch?.[1]) {
      values.push(valueMatch[1]);
    }

    if (depth === 0) {
      return {
        definition: { kind: "enum", name, values },
        nextIndex: index,
      };
    }
  }

  throw new Error(`Unclosed enum ${name}`);
}

function parseMessage(lines, startIndex, name) {
  const fields = [];
  let depth = 0;

  for (let index = startIndex; index < lines.length; index += 1) {
    const line = stripLineComment(lines[index]).trim();
    depth += count(line, "{");
    depth -= count(line, "}");

    if (index !== startIndex && line.match(/^(message|enum)\s+/)) {
      throw new Error(`Nested definitions are not supported in message ${name}`);
    }

    const field = parseField(line);
    if (field) {
      fields.push(field);
    }

    if (depth === 0) {
      return {
        definition: { kind: "message", name, fields },
        nextIndex: index,
      };
    }
  }

  throw new Error(`Unclosed message ${name}`);
}

function parseField(line) {
  if (
    line === "" ||
    line.startsWith("reserved ") ||
    line.startsWith("option ") ||
    line.startsWith("oneof ") ||
    line === "}"
  ) {
    return undefined;
  }

  const mapMatch = line.match(
    /^(optional\s+)?(repeated\s+)?map<\s*([A-Za-z0-9_.]+)\s*,\s*([A-Za-z0-9_.]+)\s*>\s+([A-Za-z0-9_]+)\s*=\s*\d+\s*(\[[^\]]+\])?\s*;/
  );
  if (mapMatch) {
    return {
      kind: "map",
      keyType: mapMatch[3],
      valueType: mapMatch[4],
      name: mapMatch[5],
      jsonName: extractJsonName(mapMatch[6]) ?? toCamelCase(mapMatch[5]),
    };
  }

  const fieldMatch = line.match(
    /^(optional\s+)?(repeated\s+)?([A-Za-z0-9_.]+)\s+([A-Za-z0-9_]+)\s*=\s*\d+\s*(\[[^\]]+\])?\s*;/
  );
  if (!fieldMatch) {
    return undefined;
  }

  return {
    kind: "field",
    repeated: Boolean(fieldMatch[2]),
    type: fieldMatch[3],
    name: fieldMatch[4],
    jsonName: extractJsonName(fieldMatch[5]) ?? toCamelCase(fieldMatch[4]),
  };
}

function extractJsonName(options) {
  return options?.match(/json_name\s*=\s*"([^"]+)"/)?.[1];
}

function count(value, needle) {
  return value.split(needle).length - 1;
}

function toCamelCase(value) {
  return value.replace(/_([a-z0-9])/g, (_, character) => character.toUpperCase());
}

function propertyKey(value) {
  return /^[A-Za-z_$][A-Za-z0-9_$]*$/.test(value) ? value : JSON.stringify(value);
}

function schemaName(name) {
  return `${name}ProtoSchema`;
}

function typeName(name) {
  return `${name}Proto`;
}

function normalizeType(type) {
  return type.startsWith(".") ? type.slice(1) : type;
}

function relativeImport(fromFilePath, toFilePath) {
  const fromDir = dirname(outputPathForProto(fromFilePath));
  const toPath = outputPathForProto(toFilePath).replace(/\.ts$/, ".js");
  let importPath = relative(fromDir, toPath);

  if (!importPath.startsWith(".")) {
    importPath = `./${importPath}`;
  }

  return importPath;
}

function outputPathForProto(filePath) {
  return join(outputRoot, relative(protoRoot, filePath)).replace(/\.proto$/, ".zod.ts");
}

function generatedTypePathForProto(filePath) {
  const outPath = outputPathForProto(filePath);
  return `./${outPath.split("/").at(-1)?.replace(/\.zod\.ts$/, ".js")}`;
}

function buildIndexes(files) {
  const definitionsByName = new Map();

  for (const file of files) {
    for (const definition of file.definitions) {
      const fullName = `${file.packageName}.${definition.name}`;
      definitionsByName.set(definition.name, { ...definition, filePath: file.filePath, fullName });
      definitionsByName.set(fullName, { ...definition, filePath: file.filePath, fullName });
    }
  }

  return { definitionsByName };
}

function renderFile(file, indexes) {
  const localDefinitions = new Map(
    file.definitions.map((definition) => [definition.name, definition])
  );
  const imports = collectImports(file, indexes, localDefinitions);
  const generatedTypeImport = generatedTypePathForProto(file.filePath);
  const lines = [generatedHeader, `import { z } from "zod";`, `import type * as pb from "${generatedTypeImport}";`];

  for (const [importPath, names] of [...imports.entries()].sort(([left], [right]) =>
    left.localeCompare(right)
  )) {
    lines.push(
      `import { ${[...names].sort().join(", ")} } from "${importPath}";`
    );
  }

  lines.push("");

  for (const definition of file.definitions.filter((item) => item.kind === "enum")) {
    lines.push(renderEnum(definition), "");
  }

  for (const definition of file.definitions.filter((item) => item.kind === "message")) {
    lines.push(renderMessage(definition, indexes, localDefinitions), "");
  }

  return `${lines.join("\n").trimEnd()}\n`;
}

function collectImports(file, indexes, localDefinitions) {
  const imports = new Map();

  for (const definition of file.definitions) {
    if (definition.kind !== "message") {
      continue;
    }

    for (const field of definition.fields) {
      const referencedType = field.kind === "map" ? field.valueType : field.type;
      const referencedDefinition = indexes.definitionsByName.get(normalizeType(referencedType));

      if (
        !referencedDefinition ||
        scalarSchemas.has(normalizeType(referencedType)) ||
        referencedDefinition.filePath === file.filePath ||
        localDefinitions.has(referencedDefinition.name)
      ) {
        continue;
      }

      const importPath = relativeImport(file.filePath, referencedDefinition.filePath);
      if (!imports.has(importPath)) {
        imports.set(importPath, new Set());
      }
      imports.get(importPath).add(schemaName(referencedDefinition.name));
    }
  }

  return imports;
}

function renderEnum(definition) {
  const values = definition.values.map((value) => JSON.stringify(value)).join(", ");

  return [
    `export const ${schemaName(definition.name)} = z.enum([${values}]) satisfies z.ZodType<pb.${definition.name}>;`,
    `export type ${typeName(definition.name)} = z.infer<typeof ${schemaName(definition.name)}>;`,
  ].join("\n");
}

function renderMessage(definition, indexes, localDefinitions) {
  const fields =
    definition.fields.length === 0
      ? ""
      : `\n${definition.fields.map((field) => renderObjectField(field, indexes, localDefinitions)).join("\n")}\n`;

  return [
    `export const ${schemaName(definition.name)} = z.object({${fields}}) satisfies z.ZodType<pb.${definition.name}>;`,
    `export type ${typeName(definition.name)} = z.infer<typeof ${schemaName(definition.name)}>;`,
  ].join("\n");
}

function renderObjectField(field, indexes, localDefinitions) {
  const valueSchema = field.kind === "map" ? renderMapSchema(field, indexes, localDefinitions) : renderFieldSchema(field, indexes, localDefinitions);

  return `  ${propertyKey(field.jsonName)}: ${valueSchema}.optional(),`;
}

function renderMapSchema(field, indexes, localDefinitions) {
  if (field.keyType !== "string") {
    throw new Error(`Only string map keys are supported for ${field.name}`);
  }

  return `z.record(z.string(), ${renderBaseSchema(field.valueType, indexes, localDefinitions)})`;
}

function renderFieldSchema(field, indexes, localDefinitions) {
  const base = renderBaseSchema(field.type, indexes, localDefinitions);
  return field.repeated ? `z.array(${base})` : base;
}

function renderBaseSchema(rawType, indexes, localDefinitions) {
  const normalizedType = normalizeType(rawType);
  const scalarSchema = scalarSchemas.get(normalizedType);

  if (scalarSchema) {
    return scalarSchema;
  }

  const referencedDefinition = indexes.definitionsByName.get(normalizedType);
  if (!referencedDefinition) {
    throw new Error(`Unknown proto type: ${rawType}`);
  }

  if (referencedDefinition.kind === "enum") {
    return schemaName(referencedDefinition.name);
  }

  if (localDefinitions.has(referencedDefinition.name)) {
    return `z.lazy(() => ${schemaName(referencedDefinition.name)})`;
  }

  return schemaName(referencedDefinition.name);
}

function main() {
  const files = walkProtoFiles(protoRoot).map(parseProtoFile);
  const indexes = buildIndexes(files);

  for (const file of files) {
    const outputPath = outputPathForProto(file.filePath);
    mkdirSync(dirname(outputPath), { recursive: true });
    writeFileSync(outputPath, renderFile(file, indexes));
  }
}

main();
